WITH unnested AS (
    SELECT
        local_date,
        fk_places,
        UNNEST(visits) AS daily_visits,
        ( EXTRACT(YEAR FROM local_date) || '-' ||
            EXTRACT(MONTH FROM local_date) || '-' ||
            UNNEST(RANGE(1, LENGTH(visits) + 1))        -- to get day in month
        )::DATE AS full_date
    FROM visits
), updated_visits AS (
    -- First, I wrote the query without the JOINS
    SELECT
        u.local_date,
        u.fk_places,
        CASE
            WHEN strftime('%a', full_date) = 'Sun' AND b.name = 'Sansom' THEN 0
            ELSE daily_visits
        END AS updated_daily_visits
    FROM unnested u
    JOIN places p
        ON p.pid = u.fk_places
    JOIN brands b 
        ON b.pid = p.fk_brands
    ORDER BY 
        local_date,
        fk_places,
        full_date               -- This is necessary to keep order of daily visits
)
SELECT 
    local_date, 
    fk_places, 
    LIST(updated_daily_visits) AS visits
FROM updated_visits
GROUP BY local_date, fk_places
ORDER BY local_date, fk_places;



--          SECOND ATTEMPT
--  This solution escapes using a GROUP BY, edits visits array in-place.
SELECT 
    v.local_date,
    v.fk_places,
    (
        WITH unnested AS (
            SELECT UNNEST(visits) AS daily_visits,
                ( 
                    EXTRACT(YEAR FROM local_date) || '-' ||
                    EXTRACT(MONTH FROM local_date) || '-' ||
                    UNNEST(RANGE(1, LENGTH(visits) + 1))        -- to get day in month
                )::DATE AS full_date
        ), updated_visits AS (
            SELECT 
                CASE
                    WHEN strftime('%a', full_date) = 'Sun' AND b.name = 'Sansom' THEN 0
                    ELSE daily_visits
                END AS updated_daily_visits
            FROM unnested
        ) SELECT LIST(updated_daily_visits)
            FROM updated_visits
    )   AS new_visits
FROM visits v
JOIN places p
        ON p.pid = v.fk_places
    JOIN brands AS b 
        ON b.pid = p.fk_brands
ORDER BY local_date, fk_places;



